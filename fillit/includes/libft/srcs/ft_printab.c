#include "libft.h"

void ft_printab(char **tab)
{
	int y;

	y = 0;
	while (tab[y] != NULL)
	{
		ft_putnbr(y);
		ft_putchar('{');
		ft_putstr(tab[y]);
		ft_putstr("}\n");
		++y;
	}
}
